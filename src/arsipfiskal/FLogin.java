    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        try{
            st=Koneksi.con.createStatement();
            //Statement statement = (Statement) Koneksi.GetConnection.createStatement();
            ResultSet RS = st.executeQuery("SELECT * FROM tb_admin WHERE "+"username='"+txtUsername.getText()+"'");
            if(RS.next()){
                if(txtPassword.getText().equals(RS.getString("password"))){
                    new FBeranda().show();
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Password Anda Salah !!");
                    txtPassword.setText("");
                    txtUsername.requestFocus();
                }
            }else{
                JOptionPane.showMessageDialog(rootPane, "Username Anda Salah !!");
                txtUsername.setText("");
                txtPassword.requestFocus();
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(rootPane, "Username dan Password Salah !!");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

